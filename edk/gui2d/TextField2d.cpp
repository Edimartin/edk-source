bool edk::gui2d::TextField2d::TextVec::write(edk::char8* str){
    this->clean();
    //test the string
    if(str){
        //get the strng lenght
        edk::uint32 lenght = edk::String::strSize(str);
        edk::char32 c;
        edk::uint8 increment=0u;
        if(lenght){
            //populate the vector
            for(edk::uint32 i=0u;i<lenght;){
                if(str[i]==13
                        ||
                        str[i]==10
                        ||
                        str[i]=='\0'
                        ){
                    break;
                }
                c = edk::String::utf8ToUint32(&str[i]);
                increment=edk::String::utf8Bytes(&str[i]);
                if(increment){
                    i+=increment;

                    //test if the lenght exceede the limit
                    if(this->vec.size()>=this->limit && this->limit){
                        return true;
                    }

                    //test the case
                    switch(this->caseInput){
                    case edk::gui2d::upperAndLowerCase:
                        break;
                    case edk::gui2d::upperCase:
                        if(c>='a' && c<='z'){
                            c+='A'-'a';
                        }
                        break;
                    case edk::gui2d::lowerCase:
                        if(c>='A' && c<='Z'){
                            c-='A'-'a';
                        }
                        break;
                    }

                    if(this->canGetIt(c)){
                        this->vec.pushBack(c);
                    }
                }
                else{

                    return false;
                }
            }
            if(this->vec.size()){
                return true;
            }
        }
    }
    return false;
}
